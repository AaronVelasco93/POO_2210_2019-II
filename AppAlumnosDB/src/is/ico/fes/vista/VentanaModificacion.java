    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        
        Alumno a = adb.buscaPorNumCta(jTextField1.getText());
        if (a != null) {
            jPanel1.setVisible(true);
            jButton1.setEnabled(false);
            jTextField2.setText(a.getNombre());
            jTextField3.setText(a.getPaterno());
            jTextField4.setText(a.getMaterno());
            jTextField5.setText("" + a.getEdad());
            jTextField6.setText(a.getNumCta());
            jTextField7.setText(a.getCarrera());
            jTextField8.setText(a.getTurno());
            jTextField9.setText("" + a.getPromedio());
            jTextField10.setText("" + a.getSemestre());

        } else {
            JOptionPane.showMessageDialog(this, "No existe");
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:

        Alumno a = new Alumno();
        a.setNombre(jTextField2.getText());
        a.setPaterno(jTextField3.getText());
        a.setMaterno(jTextField4.getText());
        a.setEdad(Integer.parseInt(jTextField5.getText()));
        a.setNumCta(jTextField6.getText());
        a.setCarrera(jTextField7.getText());
        a.setTurno(jTextField8.getText());
        a.setPromedio(Float.parseFloat(jTextField9.getText()));
        a.setSemestre(Integer.parseInt(jTextField10.getText()));
        if(adb.modificaAlumno(a)){
            System.out.println("ok");
        }else{
            System.out.println("Error");
        }
    }//GEN-LAST:event_jButton2MouseClicked

